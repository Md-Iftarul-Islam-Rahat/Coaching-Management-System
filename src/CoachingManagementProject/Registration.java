    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        try {
            String id = txtid.getText();
            String firstName = txtFirst.getText();
            String lastName = txtLast.getText();
            String nic = txtNick.getText();
            String father = txtFather.getText();
            String mother = txtMother.getText();
            String Gender;

            if (male.isSelected()) {
                Gender = "Male";
            } else {
                Gender = "Female";
            }

            String course = txtCourses.getSelectedItem().toString();
            String batch = txtbatch.getSelectedItem().toString();

            String mobile = txtMobile.getText();
            String address = txtAddress.getText();

            // TODO add your handling code here:
            Class.forName("com.mysql.jdbc.Driver");
            com.mysql.jdbc.Connection con = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/studentmanagement", "root", "123");

          pst = (com.mysql.jdbc.PreparedStatement) con.prepareStatement("update registration set first_Name=?, last_Name=?, Nic=?, father_Name=?, mother_Name=?, gender=?, course=?, batch=?, mobile=?, address=? where id=?");
            
            pst.setString(1, firstName);
            pst.setString(2, lastName);
            pst.setString(3, nic);
            pst.setString(4, father);
            pst.setString(5, mother);
            pst.setString(6, Gender);
            pst.setString(7, course);
            pst.setString(8, batch);
            pst.setString(9, mobile);
            pst.setString(10, address);
            pst.setString(11, id);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registration Updated.........");

            txtid.setText("");
            txtFirst.setText("");
            txtLast.setText("");
            txtNick.setText("");
            txtFather.setText("");
            txtMother.setText("");
            gender.clearSelection();
            txtCourses.setSelectedIndex(-1);
            txtbatch.setSelectedIndex(-1);
            txtMobile.setText("");
            txtAddress.setText("");
            studentReg_Table();

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

