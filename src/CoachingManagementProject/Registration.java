    public void studentReg_Table() {
        try {
            pst = con.prepareStatement("select * from registration");
            rs = pst.executeQuery();
            ResultSetMetaData rsm = rs.getMetaData();
            int c;
            c = rsm.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            while (rs.next()) {

                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("Id"));
                    v2.add(rs.getString("First_Name"));
                    v2.add(rs.getString("Last_Name"));
                    v2.add(rs.getString("NIC"));
                    v2.add(rs.getString("Father_Name"));
                    v2.add(rs.getString("Mother_Name"));
                    v2.add(rs.getString("Gender"));
                    v2.add(rs.getString("Course"));
                    v2.add(rs.getString("Batch"));
                    v2.add(rs.getString("Mobile"));
                    v2.add(rs.getString("Address"));

                }
                df.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

